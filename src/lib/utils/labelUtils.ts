// ── 4. 어절 경계 절단 + 조사 제거 ───────────────────────────────────────────

/** 어절 끝에 붙는 조사/연결어 — 라벨 끝에 남으면 어색함 */
const TRAILING_PARTICLE =
  /(이|가|은|는|을|를|의|에서|으로|로|와|과|에|이고|이며|하고|이어|이라|보다|부터|까지)$/

function wordCutStrip(text: string, maxLen: number): string {
  if (text.length <= maxLen) {
    return text.replace(TRAILING_PARTICLE, "").trim() || text
  }
  const words = text.split(/\s+/)
  let label = ""
  for (const word of words) {
    const candidate = label ? `${label} ${word}` : word
    if (candidate.length > maxLen) break
    label = candidate
  }
  if (!label) return text.slice(0, maxLen)
  return label.replace(TRAILING_PARTICLE, "").trim() || label
}

// ── 메인 함수 ────────────────────────────────────────────────────────────────

/**
 * 진단 문장 또는 긴 설명을 짧은 진단 라벨로 변환한다.
 *
 * @param text  원문 진단 문장 (수정하지 않음 — 반환값만 바꿈)
 * @param maxLen 최대 글자 수 (기본 20)
 */
export function makeDiagnosticLabel(text: string, maxLen = 20): string {
  const raw = text.trim()
  if (!raw) return ""

  // 접두 제거
  const stripped = stripPrefix(raw)
  if (!stripped) return raw.slice(0, maxLen)

  // 숫자 수치 제거 (예: "31초로", "0.00%로", "57,200명" 등) — 라벨 추출에 방해됨
  const t = stripped
    .replace(/[\d,]+\.?\d*\s*(%|초|분|시간|회|개|명|만|억|K|M)\s*(로|이|가|은|는|으로|이라)?/gi, "")
    .replace(/\s{2,}/g, " ")
    .trim() || stripped

  // 짧고 문장형이 아니면 그대로 반환
  if (t.length <= maxLen && !looksLikeSentence(t)) return t

  // ── 1. 전체 패턴 매칭 ─────────────────────────────────────────────────────
  for (const [re, label] of FULL_RULES) {
    if (re.test(t)) return label
  }

  // 짧지만 문장형인 경우: 위 규칙에 없으면 어절 절단으로 처리
  if (t.length <= maxLen) {
    return wordCutStrip(t, maxLen)
  }

  // ── 2. 주제어 + 상태어 조합 ────────────────────────────────────────────────
  let topic = ""
  let state = ""
  for (const [re, label] of TOPIC_RULES) {
    if (re.test(t)) {
      topic = label
      break
    }
  }
  for (const [re, label] of STATE_RULES) {
    if (re.test(t)) {
      state = label
      break
    }
  }

  if (topic && state) {
    const composed = `${topic} ${state}`
    return composed.length <= maxLen ? composed : wordCutStrip(composed, maxLen)
  }
  if (topic) return `${topic} 패턴`
  if (state) return state

  // ── 3. 어절 경계 절단 (조사 끝 회피) ─────────────────────────────────────
  return wordCutStrip(t, maxLen)
}
