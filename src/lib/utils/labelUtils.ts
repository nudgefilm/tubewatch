/**
 * 진단 문장 → 짧은 라벨(진단형, 최대 20자 기본)
 *
 * 우선순위:
 *  1. 전체 패턴 매칭 → 사전 정의 라벨
 *  2. 주제어 + 상태어 조합 → 합성 라벨
 *  3. 어절 경계 절단 (조사 끝 회피)
 *
 * - 외부 API·모델 호출 없음 — 규칙 기반만
 * - 원문 삭제 금지: 원문은 호출 측(shortReason/description/whyNeeded)에서 보존
 */

// ── 접두 제거 ────────────────────────────────────────────────────────────────

const PREFIX_RE =
  /^(주의로 기록된 접근 피하기|스냅샷 플래그|베이스 강점·패턴|베이스 주의·병목|주의·개선으로 기록|분석 약점|분석 병목)[:：\s]+/i

function stripPrefix(text: string): string {
  return text.replace(PREFIX_RE, "").trim()
}

// ── 문장 종결형 판별 ─────────────────────────────────────────────────────────
// "X입니다", "X됩니다", "X합니다", "X습니다" 등 — 진단 라벨로 그대로 쓰면 어색함

function looksLikeSentence(t: string): boolean {
  return t.length > 8 && /[다]$/.test(t)
}

// ── 1. 전체 텍스트 패턴 → 고정 라벨 ────────────────────────────────────────

